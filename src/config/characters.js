// Fun√ß√£o para obter personagens por g√©nero
export async function getCharactersByGender(gender) {
    const characters = (await import('./characters.js')).default;
    if (gender === 'all') return characters;
    return characters.filter(char => char.gender === gender);
}

// Fun√ß√£o para obter personagem aleat√≥rio (todos com mesma chance)
export function getRandomCharacter(characters) {
    return characters[Math.floor(Math.random() * characters.length)];
}

// Fun√ß√£o para obter estat√≠sticas de pontos
export function getPointsStats(characters) {
    if (!characters) return null;
    const points = characters.map(c => c.points || 0);
    return {
        total: points.reduce((sum, p) => sum + p, 0),
        average: Math.round(points.reduce((sum, p) => sum + p, 0) / points.length),
        min: Math.min(...points),
        max: Math.max(...points),
        count: characters.length
    };
}
